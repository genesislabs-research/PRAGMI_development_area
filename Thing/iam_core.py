@dataclass
class IamCoreConfig:
    """Configuration for the I-am core.

    The Davey 2016 connectivity numbers are baked in as INITIAL conditions
    for the plastic inter-region matrices. R-STDP mutates them every tick
    after construction.

    The 160 ms ignition latency is implemented in two places. The
    omega-path delay (Equation 26) is a deque holding past r_reentry
    vectors of length ceil(160 / dt_ms); Psi reads omega from the popped
    tail rather than the current head. The MPFC->PCC inhibitory return
    is a separate, biologically real conduction delay that defaults to
    one tick (the corpus does not specify the latter; the former is the
    Lou et al. (2017) measurement).

    The tonic baseline drives are NOT biological quantities. They are
    engineering scalars chosen so the populations fire on a 1 ms tick.
    They will need recalibration when dt_ms changes; that recalibration
    is the responsibility of an external calibration step, not the loop.

    The theta gate frequency f_theta_hz defaults to 6.0 Hz, the slow end
    of the human cortical theta band that paces episodic encoding
    (Buzsaki 2002). The theta phase advances internally by default; an
    external phase source can be supplied to forward() so multiple
    populations can share a phase reference.

    Reference: Davey et al. (2016). DOI: 10.1016/j.neuroimage.2016.02.022
    Reference: Lou et al. (2017). DOI: 10.1016/j.neubiorev.2016.04.004
    Reference: Buzsaki (2002). DOI: 10.1016/S0896-6273(02)00586-X
    """

    # ------ Population dimensions ------
    coordinate_dim: int = 64
    proto_self_dim: int = 64
    pcc_dim: int = 128
    mpfc_dim: int = 96
    ipl_dim: int = 96
    state_dim: int = 16

    # ------ Davey 2016 Model 27 connectivity ------
    pcc_to_mpfc_endogenous: float = 0.20
    pcc_to_mpfc_modulatory: float = 0.62
    pcc_to_ipl_endogenous: float = 0.33
    pcc_to_ipl_modulatory: float = 0.56
    ipl_to_mpfc_endogenous: float = 0.11
    mpfc_to_pcc_endogenous: float = -0.18
    mpfc_to_pcc_modulatory: float = -0.53
    self_reference_gain: float = 1.0

    proto_to_pcc_init: float = 0.15
    coord_to_pcc_init: float = 0.20

    # ------ Tonic baseline drives ------
    # NOT biological; engineering scalars retuned per dt by an external
    # calibration step. The defaults below assume dt_ms=1.0 with the
    # SSMLIFSubstrate's default threshold of 1.0.
    pcc_baseline_drive: float = 3.0
    mpfc_baseline_drive: float = 3.5
    ipl_baseline_drive: float = 3.5
    proto_baseline_drive: float = 2.5

    # ------ Persistent traces ------
    pcc_trace_tau: float = 0.92
    pcc_predicted_trace_tau: float = 0.90
    condition_trace_tau: float = 0.95
    reentry_trace_tau: float = 200.0  # ticks; Eq. 27 slow self-feed

    # ------ Mixing weights for autobio readout (Eq. 27) ------
    zeta_fast: float = 1.0
    zeta_slow: float = 0.3

    # ------ proto_self placement on PCC drive ------
    # 'inside_tanh': proto-self afferent enters PCC drive inside the
    #   tanh together with the cascade content (current default; saturates
    #   when the body-state is strong).
    # 'outside_tanh': proto-self enters as an additive drive outside the
    #   tanh (linear contribution; cannot saturate PCC drive alone).
    # 'modulator': proto-self scales the W_proto_to_pcc effective gain
    #   (multiplicative on the cascade pathway).
    proto_self_placement: str = "inside_tanh"

    # ------ Episode store ------
    novelty_threshold: float = 0.18
    store_cooldown_ticks: int = 3
    max_episodes: int = 4096
    autobio_readback_n: int = 3
    autobio_readback_scale: float = 0.4
    # Eviction policy when the store is full. 'lowest_novelty' (default)
    # is the biological-consolidation analog: keep what surprised the
    # substrate most. 'fifo' is the simpler oldest-first fallback for
    # deterministic testing.
    eviction_policy: str = "lowest_novelty"

    # ------ Conduction delays ------
    # omega-path: from r_reentry emission to omega delivery via Psi
    # (Equation 26). This is the corpus's primary 160 ms delay.
    ignition_latency_ms: float = 160.0
    # Inhibitory return path: MPFC emission to PCC inhibitory drive on
    # the next tick. Biologically, parvalbumin-ergic GABAergic
    # transmission is fast (<5 ms); default to dt_ms (one tick) is
    # within that range. Configurable for experiments that decouple
    # the two delays.
    inhibitory_return_latency_ms: float = 1.0

    # ------ Theta gate ------
    # Buzsaki (2002): cortical theta paces episodic encoding at 6-8 Hz.
    f_theta_hz: float = 6.0
    enable_theta_gate: bool = True
    # When True, R-STDP plasticity also respects the theta gate: only
    # the rising-phase contribution counts as potentiation; the
    # falling-phase contribution is scaled by -depression_ratio. When
    # False, R-STDP runs every tick regardless. Default False to
    # preserve standard plasticity dynamics; flip True to test the
    # theta-gated plasticity hypothesis.
    theta_gated_rstdp: bool = False

    # ------ R-STDP plasticity ------
    rstdp_lr: float = 0.005
    rstdp_trace_tau: float = 0.92
    rstdp_depression_ratio: float = 0.55
    weight_min: float = -1.0
    weight_max: float = 1.0
    rstdp_modulation_floor: float = 0.05

    # ------ Surrogate alpha ------
    # Per Neftci et al. (2019): the fast-sigmoid surrogate alpha is a
    # training artifact only. It controls the gradient flow during
    # backpropagation through the spiking nonlinearity in FPTT-style
    # training. AT INFERENCE TIME these scalars do not affect substrate
    # behavior because no gradients flow through the substrate; the
    # threshold dynamics are purely deterministic Heaviside crossings.
    # The values are kept here for completeness so a future training
    # pass can be configured from the same object that constructed the
    # inference-mode core.
    pcc_surrogate_alpha: float = 2.0
    proto_surrogate_alpha: float = 2.0
    mpfc_surrogate_alpha: float = 1.0
    ipl_surrogate_alpha: float = 1.0

    # ------ Substrate parameters shared across populations ------
    v_threshold_base: float = 1.0
    theta_decay: float = 0.95
    theta_increment: float = 0.5

    # ------ Maturity ------
    # Maturity is a [0, 1] scalar in omega. Two computation modes:
    # 'tick_count' (legacy): linear ramp from 0 to 1 over
    #   maturity_horizon_ticks. Simple, arbitrary horizon.
    # 'omega_variance' (default): rolling-window variance of recent
    #   omega vectors. Maturity rises as the loop's operating-condition
    #   distribution becomes stationary; falls when omega starts
    #   drifting again. Measured property of the loop's actual dynamics.
    maturity_mode: str = "omega_variance"
    maturity_horizon_ticks: int = 100_000  # only used when 'tick_count'
    maturity_window: int = 200             # only used when 'omega_variance'
    # Variance below maturity_var_low maps to maturity 1.0; variance
    # above maturity_var_high maps to maturity 0.0; linear in between.
    maturity_var_low: float = 0.005
    maturity_var_high: float = 0.05

    # ------ Phi encoding subspace ------
    encoding_dim: int = 64
    reserved_subspace_size: int = 64

    # ------ Synaptic re-entry register (Equations 29 and 30) ------
    enable_synaptic_reentry_register: bool = True
    synaptic_reentry_n_states: int = 20
    synaptic_reentry_epsilon_quant: float = 0.05

    # ------ Diagnostics ------
    # 0: empty diagnostics dict (production).
    # 1: tick + spike rates + novelty + theta + n_episodes.
    # 2: full diagnostic set (development; current default for
    #    backward compatibility with the smoke test).
    diagnostics_level: int = 2

    # ------ Numerical ------
    dt_ms: float = 1.0
    rng_seed: int = 1729

    # ------ Upstream kernel hook ------
    # Optional callable returning the current m_CA3 attractor settle
    # for use as the fast term of the autobio readout (Eq. 27). When
    # None, the local episode store's PCC-mean is substituted (which
    # is the cold-boot behavior that does not require a hippocampal
    # kernel). Signature: () -> Tensor of shape (pcc_dim,) or None.
    m_ca3_provider: Optional[Any] = None

    def ignition_latency_ticks(self) -> int:
        """Return ceil(ignition_latency_ms / dt_ms) as integer tick count.

        This is the omega-path delay (Equation 26): from r_reentry
        emission to omega delivery via Psi.
        """
        if self.dt_ms <= 0.0:
            raise ValueError(f"dt_ms must be positive, got {self.dt_ms}")
        return int(math.ceil(self.ignition_latency_ms / self.dt_ms))

    def inhibitory_return_ticks(self) -> int:
        """Return ceil(inhibitory_return_latency_ms / dt_ms).

        The MPFC -> PCC parvalbumin-ergic conduction delay. Default 1
        tick is the fast-spiking inhibitory range. Decoupled from the
        omega-path delay so the two can be set independently.
        """
        if self.dt_ms <= 0.0:
            raise ValueError(f"dt_ms must be positive, got {self.dt_ms}")
        return max(
            1, int(math.ceil(self.inhibitory_return_latency_ms / self.dt_ms))
        )

    def validate(self) -> None:
        """Sanity-check string-valued options at construction time.

        Raises ValueError on any unrecognized choice so misconfiguration
        surfaces immediately rather than at the first tick.
        """
        if self.proto_self_placement not in {
            "inside_tanh", "outside_tanh", "modulator",
        }:
            raise ValueError(
                f"proto_self_placement must be one of "
                f"'inside_tanh', 'outside_tanh', 'modulator'; "
                f"got {self.proto_self_placement!r}"
            )
        if self.eviction_policy not in {"lowest_novelty", "fifo"}:
            raise ValueError(
                f"eviction_policy must be 'lowest_novelty' or 'fifo'; "
                f"got {self.eviction_policy!r}"
            )
        if self.maturity_mode not in {"tick_count", "omega_variance"}:
            raise ValueError(
                f"maturity_mode must be 'tick_count' or "
                f"'omega_variance'; got {self.maturity_mode!r}"
            )
        if self.diagnostics_level not in {0, 1, 2}:
            raise ValueError(
                f"diagnostics_level must be 0, 1, or 2; "
                f"got {self.diagnostics_level}"
            )
        if self.maturity_var_low >= self.maturity_var_high:
            raise ValueError(
                f"maturity_var_low must be less than maturity_var_high"
            )


