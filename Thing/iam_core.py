# Loop-state format version + migration registry
# =========================================================================
#
# get_loop_state writes this integer into the payload's "format_version"
# field; load_loop_state reads it. When the schema changes (new field,
# renamed field, removed field), bump LOOP_STATE_FORMAT_VERSION and add
# a (from_version, to_version) -> migration function entry to
# _LOOP_STATE_MIGRATIONS. The migration receives the saved dict and
# must return a dict that satisfies the new schema.
#
# The state.py SoulManager owns the higher-level .soul format; this is
# the warm-tier-loop-only schema version used inside payload["warm"]
# ["identity"]. State serialization at the file level is solved by
# state.py and saveload_manager.py; this is just the schema version
# for what get_loop_state emits.

LOOP_STATE_FORMAT_VERSION: int = 1


def _migrate_loop_state(
    state: Dict[str, Any],
    from_version: int,
    to_version: int,
) -> Dict[str, Any]:
    """Adapt a saved loop-state dict from from_version to to_version.

    Currently no migrations are registered because the format is at v1.
    When a v2 ships, register a (1, 2) entry in _LOOP_STATE_MIGRATIONS
    that takes a v1 dict and returns a v2 dict.

    Raises:
        ValueError: when no migration path is available between the
            saved version and the target version.
    """
    if from_version == to_version:
        return state
    fn = _LOOP_STATE_MIGRATIONS.get((from_version, to_version))
    if fn is None:
        # Try chained migrations: from_version -> from_version+1 -> ...
        # -> to_version.
        v = from_version
        while v < to_version:
            step = _LOOP_STATE_MIGRATIONS.get((v, v + 1))
            if step is None:
                raise ValueError(
                    f"no migration path from loop-state v{v} to "
                    f"v{v + 1}; saved=v{from_version} target="
                    f"v{to_version}"
                )
            state = step(state)
            v += 1
        return state
    return fn(state)


_LOOP_STATE_MIGRATIONS: Dict[
    Tuple[int, int], Any
] = {}


