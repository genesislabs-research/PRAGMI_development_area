class WriteGateError(RuntimeError):
    """Raised when an attempt is made to write tau_proj=1 content.

    Reference: PRAGMI Complete Mathematical Corpus Equation 9d.3.
    """


class WriteGate:
    """Refuses to write tau_proj=1 content into the episode store.

    BIOLOGICAL FUNCTION: Enforces the architectural commitment that the
    past must not be guessed. The forward projection that retrieval
    produces (RetrievalDraw.draw_future) is tagged tau_proj=1 at the type
    level. Any consumer that tries to push that content back into the
    episode store gets a hard refusal here.

    Reference: PRAGMI Complete Mathematical Corpus Equations 9b.0 and
    9d.3. The contract on the past.
    """

    @staticmethod
    def write(
        episodes: List[EpisodeRecord],
        record: EpisodeRecord,
        max_episodes: int,
        eviction: str = "lowest_novelty",
    ) -> None:
        """Append record to episodes, enforcing the tau_proj=0 invariant.

        When the store is full, eviction policy decides which episode to
        drop. Lowest-novelty eviction is the biological-consolidation
        analog: episodes that surprised the substrate most are retained;
        episodes that were redundant with what the loop already knew are
        discarded first. FIFO is the simpler fallback when novelty
        comparisons are not desired (testing, deterministic eviction).

        Args:
            episodes: the episode list to mutate in place.
            record: the EpisodeRecord to write.
            max_episodes: the cap.
            eviction: "lowest_novelty" or "fifo". Default lowest_novelty.

        Raises:
            WriteGateError: if record.tau_proj != 0.
            ValueError: if eviction is not a recognized policy.
        """
        if record.tau_proj != 0:
            raise WriteGateError(
                f"WriteGate refuses tau_proj={record.tau_proj}; only "
                f"tau_proj=0 is admissible to the causal record."
            )
        if eviction not in {"lowest_novelty", "fifo"}:
            raise ValueError(
                f"eviction must be 'lowest_novelty' or 'fifo', "
                f"got {eviction!r}"
            )
        if len(episodes) >= max_episodes:
            if eviction == "fifo":
                episodes.pop(0)
            else:  # lowest_novelty
                # Find the index of the lowest-novelty existing episode.
                # Ties broken by oldest (lowest tick) so the policy is
                # deterministic across equal-novelty stores.
                min_idx = 0
                min_key = (episodes[0].novelty, episodes[0].tick)
                for i in range(1, len(episodes)):
                    key = (episodes[i].novelty, episodes[i].tick)
                    if key < min_key:
                        min_key = key
                        min_idx = i
                episodes.pop(min_idx)
        episodes.append(record)


class RetrievalDraw:
    """Distinguishes past retrieval from forward projection at the type level.

    Reference: PRAGMI Complete Mathematical Corpus Equation 9d.3.
    """

    @staticmethod
    def draw_past(
        episodes: List[EpisodeRecord],
        query_omega: Tensor,
        n: int = 1,
    ) -> List[EpisodeRecord]:
        """Past retrieval: tau_proj=0, current omega bound at read."""
        if not episodes:
            return []
        q = query_omega.detach().to(torch.float32).flatten()
        if q.numel() != 8:
            raise ValueError(
                f"query_omega must have 8 elements, got {q.numel()}"
            )
        q_norm = q / (q.norm() + 1e-8)
        scored: List[Tuple[float, EpisodeRecord]] = []
        for ep in episodes:
            o = ep.operating_condition.detach().to(torch.float32).flatten()
            o_norm = o / (o.norm() + 1e-8)
            sim = float((q_norm * o_norm).sum().item())
            scored.append((sim, ep))
        scored.sort(key=lambda pair: -pair[0])
        return [ep for _, ep in scored[:n]]

    @staticmethod
    def draw_future(
        episodes: List[EpisodeRecord],
        query_omega: Tensor,
        n: int = 1,
    ) -> List[EpisodeRecord]:
        """Forward projection: tau_proj=1."""
        past = RetrievalDraw.draw_past(episodes, query_omega, n)
        forward = []
        for ep in past:
            ep_copy = EpisodeRecord(
                tick=ep.tick,
                pcc_spikes=ep.pcc_spikes.clone(),
                mpfc_spikes=ep.mpfc_spikes.clone(),
                prior_pcc_trace=ep.prior_pcc_trace.clone(),
                predicted_pcc_trace=ep.predicted_pcc_trace.clone(),
                operating_condition=ep.operating_condition.clone(),
                novelty=ep.novelty,
                prediction_error=ep.prediction_error,
                source_sha256=ep.source_sha256,
                structural_hash=ep.structural_hash,
                tau_proj=1,
            )
            forward.append(ep_copy)
        return forward


