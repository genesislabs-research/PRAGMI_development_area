@dataclass
class RSTDPConfig:
    """Configuration for the three-factor reward-modulated STDP rule.

    Reference: Florian, R.V. (2007). DOI: 10.1162/neco.2007.19.6.1468.
    """

    lr: float = 0.005
    trace_tau: float = 0.92
    depression_ratio: float = 0.55
    weight_min: float = -1.0
    weight_max: float = 1.0
    modulation_floor: float = 0.05


class RSTDPRule(nn.Module):
    """Three-factor reward-modulated STDP for one inter-region projection.

    BIOLOGICAL STRUCTURE: Synaptic plasticity at one cortico-cortical
    projection. Pre-trace and post-trace are eligibility traces (Florian
    2007); modulation is a third factor (typically dopamine, sometimes
    norepinephrine) that gates which trace coincidences become persistent
    weight changes.

    Reference: Florian, R.V. (2007). DOI: 10.1162/neco.2007.19.6.1468.
    """

    def __init__(
        self,
        pre_dim: int,
        post_dim: int,
        cfg: RSTDPConfig,
    ) -> None:
        super().__init__()
        self.cfg = cfg
        self.register_buffer("pre_trace", torch.zeros(pre_dim))
        self.register_buffer("post_trace", torch.zeros(post_dim))

    @torch.no_grad()
    def update(
        self,
        weight: Tensor,
        pre_spikes: Tensor,
        post_spikes: Tensor,
        modulation: float,
    ) -> None:
        """Apply one weight update in-place.

        Args:
            weight: (post_dim, pre_dim) matrix to mutate.
            pre_spikes: (B, pre_dim) or (pre_dim,) presynaptic spikes.
            post_spikes: (B, post_dim) or (post_dim,) postsynaptic spikes.
            modulation: scalar reward-modulation factor. Sign preserved;
                absolute value floored at cfg.modulation_floor.
        """
        m_signed = float(modulation)
        m = max(abs(m_signed), self.cfg.modulation_floor)
        if m_signed < 0:
            m = -m
        device = weight.device
        pre = pre_spikes.detach().to(device).float()
        post = post_spikes.detach().to(device).float()
        if pre.dim() > 1:
            pre = pre.mean(dim=0)
        if post.dim() > 1:
            post = post.mean(dim=0)
        if pre.numel() != weight.shape[1]:
            raise ValueError(
                f"R-STDP shape mismatch: weight={tuple(weight.shape)} "
                f"pre={tuple(pre.shape)}"
            )
        if post.numel() != weight.shape[0]:
            raise ValueError(
                f"R-STDP shape mismatch: weight={tuple(weight.shape)} "
                f"post={tuple(post.shape)}"
            )
        if self.pre_trace.device != device:
            self.pre_trace = self.pre_trace.to(device)
            self.post_trace = self.post_trace.to(device)
        # Snapshot traces BEFORE updating; pre-before-post and
        # post-before-pre coincidences read the old traces.
        old_pre = self.pre_trace.clone()
        old_post = self.post_trace.clone()
        self.pre_trace.mul_(self.cfg.trace_tau).add_(pre)
        self.post_trace.mul_(self.cfg.trace_tau).add_(post)
        # LTP: post fires while pre-trace is still high.
        ltp = torch.outer(post, old_pre)
        # LTD: pre fires while post-trace is still high.
        ltd = torch.outer(old_post, pre)
        delta = self.cfg.lr * m * (
            ltp - self.cfg.depression_ratio * ltd
        )
        weight.add_(delta)
        weight.clamp_(min=self.cfg.weight_min, max=self.cfg.weight_max)

    def reset(self) -> None:
        """Zero the eligibility traces."""
        self.pre_trace.zero_()
        self.post_trace.zero_()

    def serialize(self) -> Dict[str, Tensor]:
        """Return CPU-side trace state."""
        return {
            "pre_trace": self.pre_trace.detach().cpu().clone(),
            "post_trace": self.post_trace.detach().cpu().clone(),
        }

    def restore(self, state: Dict[str, Tensor]) -> None:
        """Restore traces from a serialized dict."""
        self.pre_trace.copy_(
            state["pre_trace"].to(self.pre_trace.device),
        )
        self.post_trace.copy_(
            state["post_trace"].to(self.post_trace.device),
        )


