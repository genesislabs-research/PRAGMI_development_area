@dataclass
class OperatingCondition:
    """The eight-component operating-condition vector omega(t) the loop emits.

    BIOLOGICAL NAME: neuromodulatory operating state plus loop-derived
    dynamic-state scalars.

    PLAIN ENGLISH: a single eight-vector that summarizes what chemical
    operating mode the loop is in (DA/NE/ACh/5-HT) plus what dynamic regime
    its own ignitions are showing right now (novelty, arousal, valence,
    maturity). Every episodic write is bound to this vector at write time
    so the content cannot be retrieved separately from the operating
    condition under which it was stored.

    Reference: Yu, A.J. and Dayan, P. (2005). "Uncertainty,
    neuromodulation, and attention." Neuron, 46(4), 681-692.
    DOI: 10.1016/j.neuron.2005.04.026

    Reference: Hasselmo, M.E. (2006). "The role of acetylcholine in
    learning and memory." Current Opinion in Neurobiology, 16(6), 710-715.
    DOI: 10.1016/j.conb.2006.09.002
    """

    da: float = 0.0
    ne: float = 0.0
    ach: float = 0.5
    five_ht: float = 0.0
    novelty: float = 0.0
    arousal: float = 0.0
    valence: float = 0.0
    maturity: float = 0.0

    def to_tensor(self, device: torch.device | str = "cpu") -> Tensor:
        """Return the canonical 8-vector representation."""
        return torch.tensor(
            [self.da, self.ne, self.ach, self.five_ht,
             self.novelty, self.arousal, self.valence, self.maturity],
            dtype=torch.float32,
            device=device,
        )

    @classmethod
    def from_tensor(cls, t: Tensor) -> "OperatingCondition":
        """Reconstruct from a saved 8-vector."""
        v = t.detach().cpu().tolist()
        if len(v) != 8:
            raise ValueError(
                f"OperatingCondition tensor must have 8 elements, "
                f"got {len(v)}"
            )
        return cls(
            da=v[0], ne=v[1], ach=v[2], five_ht=v[3],
            novelty=v[4], arousal=v[5], valence=v[6], maturity=v[7],
        )


class Phi(nn.Module):
    """Fixed projection of omega into a reserved subspace of the encoding.

    BIOLOGICAL FUNCTION: Phi is the structural binding operator that fixes
    how the operating-condition vector occupies the reserved dimensions of
    every stored encoding. It is fixed at construction and not learnable.
    A learned Phi would let the system minimize the operating-condition
    contribution under training pressure, which is the exact failure mode
    the binding exists to prevent.

    Reference: PRAGMI Complete Mathematical Corpus, Section 9b Engineering
    Note. The fixed-Phi commitment.
    """

    def __init__(
        self,
        encoding_dim: int,
        reserved_subspace_size: Optional[int] = None,
    ) -> None:
        super().__init__()
        self.encoding_dim = encoding_dim
        if reserved_subspace_size is None:
            reserved_subspace_size = max(8, encoding_dim // 8)
        if reserved_subspace_size < 8:
            raise ValueError(
                "reserved_subspace_size must be at least 8 to hold the "
                "eight-component operating condition"
            )
        self.reserved_subspace_size = reserved_subspace_size
        block = reserved_subspace_size // 8
        proj = torch.zeros(8, encoding_dim, dtype=torch.float32)
        for i in range(8):
            start = i * block
            end = start + block
            proj[i, start:end] = 1.0 / math.sqrt(block)
        # NOT a Parameter; this is fixed at construction.
        self.register_buffer("projection", proj)

    def forward(self, omega: Tensor) -> Tensor:
        """Project omega into the encoding subspace.

        Args:
            omega: (8,) or (B, 8) operating-condition vector.

        Returns:
            (encoding_dim,) or (B, encoding_dim) binding vector.
        """
        if omega.dim() == 1:
            return omega @ self.projection
        return omega @ self.projection


