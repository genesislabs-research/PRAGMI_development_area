@dataclass
class EpisodeRecord:
    """One stored ignition, bound to omega at write time.

    BIOLOGICAL FUNCTION: Stores the PCC ignition pattern that triggered
    the write, the MPFC inhibitory state regulating it, the
    operating-condition vector at the moment of the write, the prior PCC
    trace, the predicted PCC trace, the source-content audit hash, and
    the structural-architecture hash so loading a checkpoint into a
    structurally different substrate is detectable.

    Reference: PRAGMI Complete Mathematical Corpus, Section 9b. The omega
    binding is non-learnable structural; the source_sha256 is the audit
    trail; the structural_hash is the architectural integrity check.
    """

    tick: int
    pcc_spikes: Tensor
    mpfc_spikes: Tensor
    prior_pcc_trace: Tensor
    predicted_pcc_trace: Tensor
    operating_condition: Tensor
    novelty: float
    prediction_error: float
    source_sha256: str
    structural_hash: str
    tau_proj: int = 0  # 0 = causal record, 1 = forward projection

    def to_state(self) -> Dict[str, Any]:
        """Return a serialization-friendly dict."""
        return {
            "tick": self.tick,
            "pcc_spikes": self.pcc_spikes.detach().cpu().clone(),
            "mpfc_spikes": self.mpfc_spikes.detach().cpu().clone(),
            "prior_pcc_trace": (
                self.prior_pcc_trace.detach().cpu().clone()
            ),
            "predicted_pcc_trace": (
                self.predicted_pcc_trace.detach().cpu().clone()
            ),
            "operating_condition": (
                self.operating_condition.detach().cpu().clone()
            ),
            "novelty": float(self.novelty),
            "prediction_error": float(self.prediction_error),
            "source_sha256": str(self.source_sha256),
            "structural_hash": str(self.structural_hash),
            "tau_proj": int(self.tau_proj),
        }

    @classmethod
    def from_state(cls, state: Dict[str, Any]) -> "EpisodeRecord":
        """Reconstruct from a serialized dict."""
        return cls(
            tick=int(state["tick"]),
            pcc_spikes=state["pcc_spikes"],
            mpfc_spikes=state["mpfc_spikes"],
            prior_pcc_trace=state["prior_pcc_trace"],
            predicted_pcc_trace=state["predicted_pcc_trace"],
            operating_condition=state["operating_condition"],
            novelty=float(state["novelty"]),
            prediction_error=float(state["prediction_error"]),
            source_sha256=str(state["source_sha256"]),
            structural_hash=str(state["structural_hash"]),
            tau_proj=int(state.get("tau_proj", 0)),
        )


