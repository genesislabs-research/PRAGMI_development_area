@dataclass
class SynapticReentryConfig:
    """Configuration for the discrete-state synaptic re-entry register.

    BIOLOGICAL FUNCTION: Encodes the regulated re-entry signal
    r_reentry(t) into a finite set of discrete synaptic weight states so
    the signal survives the 160 ms ignition-delay buffer without
    floating-point drift. Standard floating-point matrices accumulate
    rounding error on every addition; over 160 ticks at 1 ms that drift
    is small but not zero, and the corpus 11a commitment is that the
    monitoring error written into the loop's re-entry pathway must be
    stable on the order of the ignition delay.

    Reference: PRAGMI Complete Mathematical Corpus, subsection 11a.
    Khrulev, S., Lepleux, M. and Bhattacharya, B.S. (2019). "Turing
    complete neural computation based on synaptic plasticity." PLOS ONE,
    14(10): e0223451. DOI: 10.1371/journal.pone.0223451

    Attributes:
        n_states: number of discrete weight states K. Default 20 per the
            corpus 11a default.
        weight_min: lower bound of the weight register's value range.
        weight_max: upper bound.
        epsilon_quant: quantization step for the monitoring-error counter
            of Equation 30. Default 0.05 gives roughly 20 distinguishable
            error levels across the unit interval.
        timing_window_pre_ms: lower bound of the qualifying pre-post
            spike-timing window (negative, ms). Default -20 ms per
            Khrulev et al. 2019.
        timing_window_post_ms: upper bound of the qualifying window.
            Default +5 ms per Khrulev et al. 2019.
        update_floor: minimum absolute step the STDP transition function
            takes per qualifying spike pair. Engineering floor that keeps
            the register from getting permanently stuck at a state.
            NOT a biological quantity.
    """

    n_states: int = 20
    weight_min: float = -1.0
    weight_max: float = 1.0
    epsilon_quant: float = 0.05
    timing_window_pre_ms: float = -20.0
    timing_window_post_ms: float = 5.0
    update_floor: float = 1e-3


class SynapticReentryRegister(nn.Module):
    """Finite-state STDP register encoding r_reentry across the 160 ms delay.

    BIOLOGICAL STRUCTURE: A population of synaptic weights w_re each
    taking values in a discrete set {w_0, w_1, ..., w_K}, updated by a
    generalized STDP transition function f_STDP that takes the current
    weight state and the pre-post spike timing difference and returns
    the next weight state. The state set is finite, so the register is
    immune to floating-point accumulation error during the 160 ms delay
    buffer transit.

    BIOLOGICAL FUNCTION: Implements Equations 29 and 30 of the corpus
    subsection 11a. Each tick the register receives the current
    r_reentry signal and a synthetic pre-post timing difference derived
    from the signal's per-channel sign (positive r_reentry component
    means the post-synaptic side fired before the pre-synaptic side at
    the corresponding loop position; negative means the reverse). The
    transition function snaps the current weight state to the nearest
    discrete level admissible by the timing window. The quantized error
    counter from Equation 30 encodes the absolute magnitude of
    r_reentry into the sequence (1 - 1/2^k), which is exactly
    representable as a rational number in a single weight register.

    Reference: PRAGMI Complete Mathematical Corpus subsection 11a.
    Khrulev, S., Lepleux, M. and Bhattacharya, B.S. (2019). "Turing
    complete neural computation based on synaptic plasticity." PLOS ONE,
    14(10): e0223451. DOI: 10.1371/journal.pone.0223451

    INTERFACE CONTRACT:
        Inputs:
            cfg: SynapticReentryConfig.
            n_channels: width of the r_reentry signal (= pcc_dim).

        Outputs:
            update(r_reentry): advance the register by one tick. Writes
                in-place to the weight register and to the quantized
                error counter.
            read(): return the current register state as a continuous-
                valued tensor for use by downstream consumers (Psi
                reading the delayed re-entry signal in Equation 26).
            serialize() / restore(state): warm-tier serialization.

        State:
            weights: (n_channels,) integer tensor of state indices into
                the discrete weight ladder.
            quantized_error: (n_channels,) tensor of k(t) values per
                Equation 30.
            ladder: the 1d tensor of discrete weight values {w_0, ..., w_K}.
    """

    def __init__(
        self,
        cfg: SynapticReentryConfig,
        n_channels: int,
    ) -> None:
        super().__init__()
        if cfg.n_states < 2:
            raise ValueError(
                f"n_states must be at least 2, got {cfg.n_states}"
            )
        if cfg.weight_min >= cfg.weight_max:
            raise ValueError(
                f"weight_min must be less than weight_max"
            )
        if cfg.epsilon_quant <= 0.0:
            raise ValueError(
                f"epsilon_quant must be positive, "
                f"got {cfg.epsilon_quant}"
            )
        self.cfg = cfg
        self.n_channels = n_channels
        # Discrete weight ladder. K+1 states; here n_states denotes K so
        # the ladder has K+1 entries running from weight_min to weight_max
        # inclusive.
        ladder = torch.linspace(
            cfg.weight_min, cfg.weight_max, cfg.n_states + 1,
            dtype=torch.float32,
        )
        self.register_buffer("ladder", ladder)
        # Per-channel current state index. Initialized to the middle
        # (zero crossing if min and max are symmetric).
        mid = cfg.n_states // 2
        self.register_buffer(
            "state_index",
            torch.full((n_channels,), mid, dtype=torch.long),
        )
        # Quantized error counter k(t) per Equation 30.
        self.register_buffer(
            "quantized_error",
            torch.zeros(n_channels, dtype=torch.long),
        )

    @torch.no_grad()
    def update(self, r_reentry_per_channel: Tensor) -> None:
        """Advance the register by one tick from a per-channel re-entry.

        The transition function f_STDP from Equation 29 is implemented as
        a sign-following snap: the channel's state index moves toward
        the ladder entry whose value is closest to the qualifying step
        size. The qualifying timing window enters here as a magnitude
        gate: per-channel updates with absolute value below update_floor
        produce no state change (no qualifying pre-post pair fired in
        the [-20, +5] ms window).

        The quantized error counter k(t) from Equation 30 is computed
        per-channel as floor(|r_reentry_channel| / epsilon_quant), and
        the encoded error 1 - 1/2^k is what the register exposes through
        read().

        Args:
            r_reentry_per_channel: (n_channels,) channel-mean of the
                regulated re-entry signal for this tick.

        Raises:
            ValueError: on shape mismatch.
        """
        x = r_reentry_per_channel.detach().to(self.ladder.device).float()
        if x.dim() > 1:
            x = x.flatten()
        if x.numel() != self.n_channels:
            raise ValueError(
                f"r_reentry_per_channel must have {self.n_channels} "
                f"elements, got {x.numel()}"
            )
        # Equation 30: quantized error counter.
        k = torch.floor(
            x.abs() / self.cfg.epsilon_quant,
        ).clamp(min=0).long()
        self.quantized_error.copy_(k)

        # Equation 29: state-index transition. Determine the desired
        # weight from the signed r_reentry, find the closest ladder
        # entry, move state_index by one step toward it. One-step
        # transitions per tick are the corpus default (the register
        # tracks slowly-varying signals by design).
        target_value = x.clamp(self.cfg.weight_min, self.cfg.weight_max)
        # Find nearest ladder entry per channel.
        # Shape contract: ladder is (K+1,), target is (n_channels,).
        # Output target_idx is (n_channels,) of long.
        diffs = (
            self.ladder.unsqueeze(0) - target_value.unsqueeze(1)
        ).abs()
        target_idx = diffs.argmin(dim=1)
        # Mask channels with sub-floor magnitude: no qualifying pre-post
        # pair fired this tick.
        qualifying = x.abs() >= self.cfg.update_floor
        # Move state_index by sign(target - state) where the difference
        # exists and qualifying is True.
        delta = torch.sign(
            (target_idx - self.state_index).float(),
        ).long()
        delta = delta * qualifying.long()
        new_idx = (self.state_index + delta).clamp(
            min=0, max=self.cfg.n_states,
        )
        self.state_index.copy_(new_idx)

    def read(self) -> Tensor:
        """Return the current register state as a continuous-valued tensor.

        The output is the per-channel ladder value for the current state
        index, shape (n_channels,). Downstream consumers (Psi at the
        omega-path delay tail) read this in place of the original
        continuous r_reentry to get a noise-stable encoding.
        """
        return self.ladder[self.state_index]

    def read_quantized_error(self) -> Tensor:
        """Return 1 - 1/2^k(t) per channel from Equation 30."""
        k = self.quantized_error.float()
        # 1 - 2^(-k). Use exp2 for precision, but cap k to avoid
        # underflow that would put 2^(-k) below float32 epsilon.
        k_capped = k.clamp(max=30.0)
        return 1.0 - torch.pow(2.0, -k_capped)

    def reset(self) -> None:
        """Reset register to mid-ladder and zero the error counter."""
        mid = self.cfg.n_states // 2
        self.state_index.fill_(mid)
        self.quantized_error.zero_()

    def serialize(self) -> Dict[str, Tensor]:
        """Return CPU-side state for the warm tier."""
        return {
            "state_index": self.state_index.detach().cpu().clone(),
            "quantized_error": (
                self.quantized_error.detach().cpu().clone()
            ),
        }

    def restore(self, state: Dict[str, Tensor]) -> None:
        """Restore from a serialized dict."""
        self.state_index.copy_(
            state["state_index"].to(self.state_index.device),
        )
        self.quantized_error.copy_(
            state["quantized_error"].to(self.quantized_error.device),
        )


