class IamCore(nn.Module):
    """The continuously-running recurrent self-loop.

    BIOLOGICAL STRUCTURE: A push-pull system between the posterior
    cingulate broadcast driver (PCC), the medial prefrontal inhibitory
    valve (MPFC) with parvalbumin-ergic GABAergic semantics, the inferior
    parietal lateral integrator (IPL), and the subcortical proto-self
    interoceptive afferent integrator. The MPFC inhibition reaches PCC via
    a conduction-delay buffer of length ceil(160 / dt_ms).

    Reference: Lou et al. (2017). DOI: 10.1016/j.neubiorev.2016.04.004
    Reference: Davey et al. (2016). DOI: 10.1016/j.neuroimage.2016.02.022
    Reference: Northoff et al. (2006). DOI: 10.1016/j.neuroimage.2005.12.002
    Reference: Damasio (1999). The Feeling of What Happens.
    Reference: Florian (2007). DOI: 10.1162/neco.2007.19.6.1468
    Reference: Lisman and Grace (2005). DOI: 10.1016/j.neuron.2005.05.002
    """

    def __init__(self, cfg: Optional[IamCoreConfig] = None) -> None:
        super().__init__()
        self.cfg = cfg or IamCoreConfig()
        # Validate string-valued config choices at construction so any
        # misconfiguration surfaces here rather than at the first tick.
        self.cfg.validate()

        gen = torch.Generator().manual_seed(self.cfg.rng_seed)

        # ANATOMICAL: brainstem/PAG/hypothalamus interoceptive integrator
        # projecting upward to PCC via the lamina-I-to-VMpo-to-posterior-
        # insula pathway (Craig 2002). The proto_self provides the bodily
        # anchor that tags every PCC ignition with the body-state it
        # happened to.
        torch.manual_seed(self.cfg.rng_seed + 100)
        self.proto_self = SSMLIFSubstrate(
            num_neurons=self.cfg.proto_self_dim,
            n_dim=self.cfg.state_dim,
            dt=self.cfg.dt_ms,
            v_threshold_base=self.cfg.v_threshold_base,
            theta_decay=self.cfg.theta_decay,
            theta_increment=self.cfg.theta_increment,
            surrogate_alpha=self.cfg.proto_surrogate_alpha,
        )

        # ANATOMICAL: PCC, the broadcast driver. Cingulum bundle to MPFC
        # and IPL (Davey 2016 DCM Model 27). The structural engine.
        torch.manual_seed(self.cfg.rng_seed + 200)
        self.pcc = SSMLIFSubstrate(
            num_neurons=self.cfg.pcc_dim,
            n_dim=self.cfg.state_dim,
            dt=self.cfg.dt_ms,
            v_threshold_base=self.cfg.v_threshold_base,
            theta_decay=self.cfg.theta_decay,
            theta_increment=self.cfg.theta_increment,
            surrogate_alpha=self.cfg.pcc_surrogate_alpha,
        )

        # ANATOMICAL: MPFC, inhibitory valve. Parvalbumin-ergic GABAergic
        # semantics on the PCC return path. 160 ms conduction delay
        # (Lou et al. 2017's simultaneous ignition latency).
        torch.manual_seed(self.cfg.rng_seed + 300)
        self.mpfc = SSMLIFSubstrate(
            num_neurons=self.cfg.mpfc_dim,
            n_dim=self.cfg.state_dim,
            dt=self.cfg.dt_ms,
            v_threshold_base=self.cfg.v_threshold_base,
            theta_decay=self.cfg.theta_decay,
            theta_increment=self.cfg.theta_increment,
            surrogate_alpha=self.cfg.mpfc_surrogate_alpha,
        )

        # ANATOMICAL: IPL, the third midline node. Davey (2016) Model 27
        # demonstrates a two-node approximation cannot reproduce.
        torch.manual_seed(self.cfg.rng_seed + 400)
        self.ipl = SSMLIFSubstrate(
            num_neurons=self.cfg.ipl_dim,
            n_dim=self.cfg.state_dim,
            dt=self.cfg.dt_ms,
            v_threshold_base=self.cfg.v_threshold_base,
            theta_decay=self.cfg.theta_decay,
            theta_increment=self.cfg.theta_increment,
            surrogate_alpha=self.cfg.ipl_surrogate_alpha,
        )

        # Six plastic inter-region matrices, Davey-initialized.
        self.W_coord_to_pcc = self._init_dense(
            self.cfg.pcc_dim, self.cfg.coordinate_dim,
            mean=self.cfg.coord_to_pcc_init, std=0.04, gen=gen,
        )
        self.W_proto_to_pcc = self._init_dense(
            self.cfg.pcc_dim, self.cfg.proto_self_dim,
            mean=self.cfg.proto_to_pcc_init, std=0.04, gen=gen,
        )
        pcc_mpfc_scale = (
            self.cfg.pcc_to_mpfc_endogenous
            + self.cfg.self_reference_gain
            * self.cfg.pcc_to_mpfc_modulatory
        )
        self.W_pcc_to_mpfc = self._init_dense(
            self.cfg.mpfc_dim, self.cfg.pcc_dim,
            mean=pcc_mpfc_scale, std=0.05, gen=gen,
        )
        pcc_ipl_scale = (
            self.cfg.pcc_to_ipl_endogenous
            + self.cfg.self_reference_gain
            * self.cfg.pcc_to_ipl_modulatory
        )
        self.W_pcc_to_ipl = self._init_dense(
            self.cfg.ipl_dim, self.cfg.pcc_dim,
            mean=pcc_ipl_scale, std=0.05, gen=gen,
        )
        self.W_ipl_to_mpfc = self._init_dense(
            self.cfg.mpfc_dim, self.cfg.ipl_dim,
            mean=self.cfg.ipl_to_mpfc_endogenous, std=0.04, gen=gen,
        )
        mpfc_pcc_scale = (
            self.cfg.mpfc_to_pcc_endogenous
            + self.cfg.self_reference_gain
            * self.cfg.mpfc_to_pcc_modulatory
        )
        self.W_mpfc_to_pcc = self._init_dense(
            self.cfg.pcc_dim, self.cfg.mpfc_dim,
            mean=mpfc_pcc_scale, std=0.04, gen=gen,
        )

        # Six R-STDP rules, one per plastic matrix.
        rstdp_cfg = RSTDPConfig(
            lr=self.cfg.rstdp_lr,
            trace_tau=self.cfg.rstdp_trace_tau,
            depression_ratio=self.cfg.rstdp_depression_ratio,
            weight_min=self.cfg.weight_min,
            weight_max=self.cfg.weight_max,
            modulation_floor=self.cfg.rstdp_modulation_floor,
        )
        self.rstdp_coord_to_pcc = RSTDPRule(
            self.cfg.coordinate_dim, self.cfg.pcc_dim, rstdp_cfg,
        )
        self.rstdp_proto_to_pcc = RSTDPRule(
            self.cfg.proto_self_dim, self.cfg.pcc_dim, rstdp_cfg,
        )
        self.rstdp_pcc_to_mpfc = RSTDPRule(
            self.cfg.pcc_dim, self.cfg.mpfc_dim, rstdp_cfg,
        )
        self.rstdp_pcc_to_ipl = RSTDPRule(
            self.cfg.pcc_dim, self.cfg.ipl_dim, rstdp_cfg,
        )
        self.rstdp_ipl_to_mpfc = RSTDPRule(
            self.cfg.ipl_dim, self.cfg.mpfc_dim, rstdp_cfg,
        )
        self.rstdp_mpfc_to_pcc = RSTDPRule(
            self.cfg.mpfc_dim, self.cfg.pcc_dim, rstdp_cfg,
        )

        # Persistent population-level traces.
        self.register_buffer(
            "pcc_trace", torch.zeros(self.cfg.pcc_dim),
        )
        self.register_buffer(
            "pcc_predicted_trace", torch.zeros(self.cfg.pcc_dim),
        )
        # condition_trace: clean zeros buffer so .to(device, dtype) on
        # the parent Module moves it correctly. The trace is updated in
        # place by EMA of emitted omega vectors on theta-admitted ticks.
        self.register_buffer(
            "condition_trace", torch.zeros(8, dtype=torch.float32),
        )
        # Slow self-feed trace (Eq. 27).
        self.register_buffer(
            "reentry_trace",
            torch.zeros(1, self.cfg.pcc_dim),
        )
        self._reentry_decay = math.exp(
            -1.0 / max(self.cfg.reentry_trace_tau, 1.0),
        )

        # ----- IPL-to-r_reentry projection (Equation 25 third term) -----
        # The corpus places a third term on r_reentry that brings the
        # IPL contribution into the regulated re-entry signal. The IPL
        # output lives in IPL space (ipl_dim) and r_reentry lives in
        # PCC space (pcc_dim), so the term needs a fixed projection
        # from IPL space to PCC space. The projection is non-learnable
        # by the same reasoning as Phi: a learned projection here would
        # let R-STDP minimize the IPL contribution under training
        # pressure, defeating the purpose of having a third node in
        # the loop. The init scale is small (orthogonal-style) so the
        # IPL contribution starts at the same magnitude regime as the
        # other r_reentry components without dominating.
        ipl_reentry_proj = torch.randn(
            self.cfg.pcc_dim, self.cfg.ipl_dim, generator=gen,
        ) * (1.0 / math.sqrt(self.cfg.ipl_dim))
        self.register_buffer("W_ipl_to_reentry", ipl_reentry_proj)

        # ----- Omega-path delay buffer (Equation 26) -----
        # The corpus's tau_160 sits between r_reentry emission and
        # omega delivery via Psi. Implemented as a deque holding past
        # r_reentry vectors of length ceil(160 / dt_ms). Psi reads from
        # the popped tail of this deque; when the deque is not yet full
        # (the first ignition_latency_ticks of cold boot), Psi reads
        # zeros, which represents the loop having no past re-entry to
        # base omega on yet.
        self._omega_delay_ticks = self.cfg.ignition_latency_ticks()
        self._omega_delay_buffer: Deque[Tensor] = deque(
            maxlen=self._omega_delay_ticks,
        )

        # ----- Inhibitory return path delay -----
        # MPFC -> PCC conduction delay. Decoupled from the omega-path
        # delay; defaults to 1 tick per the parvalbumin-ergic
        # fast-spiking biology. Configurable via
        # cfg.inhibitory_return_latency_ms.
        self._inhibitory_delay_ticks = self.cfg.inhibitory_return_ticks()
        self._mpfc_delay_buffer: Deque[Tensor] = deque(
            maxlen=self._inhibitory_delay_ticks,
        )

        # ----- Synaptic re-entry register (Equations 29 and 30) -----
        # Discrete-state STDP encoding of the re-entry signal so it
        # survives the omega-path delay buffer transit without
        # floating-point drift. The register is updated every tick
        # from the current r_reentry; Psi reads from the register at
        # the omega-path delay tail rather than from the float deque
        # when enable_synaptic_reentry_register is True.
        self.synaptic_reentry: Optional[SynapticReentryRegister] = None
        if self.cfg.enable_synaptic_reentry_register:
            srcfg = SynapticReentryConfig(
                n_states=self.cfg.synaptic_reentry_n_states,
                weight_min=self.cfg.weight_min,
                weight_max=self.cfg.weight_max,
                epsilon_quant=self.cfg.synaptic_reentry_epsilon_quant,
            )
            self.synaptic_reentry = SynapticReentryRegister(
                cfg=srcfg, n_channels=self.cfg.pcc_dim,
            )

        # Counters and theta phase.
        self.register_buffer(
            "tick", torch.tensor(0, dtype=torch.long),
        )
        self.register_buffer(
            "last_store_tick",
            torch.tensor(-(10 ** 9), dtype=torch.long),
        )
        self._theta_phase: float = 0.0

        # Per-population substrate states (lazy).
        self._proto_state: Optional[SubstrateState] = None
        self._pcc_state: Optional[SubstrateState] = None
        self._mpfc_state: Optional[SubstrateState] = None
        self._ipl_state: Optional[SubstrateState] = None

        # Episode store.
        self._episodes: List[EpisodeRecord] = []

        # Phi.
        self.phi = Phi(
            encoding_dim=self.cfg.encoding_dim,
            reserved_subspace_size=self.cfg.reserved_subspace_size,
        )

        # ----- Maturity machinery -----
        # tick_count: legacy linear ramp.
        self._tick_count_for_maturity: int = 0
        # omega_variance: rolling window of recent omega vectors.
        # Implemented as a deque of (8,) tensors of length
        # cfg.maturity_window. Maturity is computed as a clipped linear
        # function of the trace of the omega covariance.
        self._omega_window: Deque[Tensor] = deque(
            maxlen=self.cfg.maturity_window,
        )

    def _init_dense(
        self,
        post: int,
        pre: int,
        mean: float,
        std: float,
        gen: torch.Generator,
    ) -> nn.Parameter:
        """Construct plastic dense projection initialized normal(mean, std)."""
        w = torch.randn(post, pre, generator=gen) * std + mean
        return nn.Parameter(w, requires_grad=False)

    def _ensure_substrate_states(
        self, batch: int, device: torch.device,
    ) -> None:
        """Lazy-initialize per-population substrate states on first forward."""

        def _needs_init(s: Optional[SubstrateState]) -> bool:
            if s is None:
                return True
            if s.h.shape[0] != batch:
                return True
            if s.h.device != device:
                return True
            return False

        reinit = False
        if _needs_init(self._proto_state):
            self._proto_state = self.proto_self.init_state(
                batch_size=batch, device=device,
            )
            reinit = True
        if _needs_init(self._pcc_state):
            self._pcc_state = self.pcc.init_state(
                batch_size=batch, device=device,
            )
            reinit = True
        if _needs_init(self._mpfc_state):
            self._mpfc_state = self.mpfc.init_state(
                batch_size=batch, device=device,
            )
            reinit = True
        if _needs_init(self._ipl_state):
            self._ipl_state = self.ipl.init_state(
                batch_size=batch, device=device,
            )
            reinit = True
        if self.reentry_trace.shape[0] != batch:
            self.reentry_trace = torch.zeros(
                batch, self.cfg.pcc_dim, device=device,
            )
            reinit = True
        if reinit:
            self._mpfc_delay_buffer.clear()
            self._omega_delay_buffer.clear()
            self._omega_window.clear()
            if self.synaptic_reentry is not None:
                self.synaptic_reentry.reset()

    def forward(
        self,
        coords: Tensor,
        interoceptive: Optional[Tensor] = None,
        external_neuromod: Optional[Tensor] = None,
        theta_phase: Optional[float] = None,
    ) -> IamLoopOutput:
        """One tick of the I-am core.

        Tick order: proto-self forward, then PCC forward (using the
        delayed MPFC inhibition popped from the inhibitory return
        deque), then IPL forward, then MPFC forward, then push the new
        MPFC drive onto the inhibitory return deque, then assemble
        r_reentry from the three corpus terms (Equation 25), then push
        r_reentry onto the omega-path delay deque and update the
        synaptic re-entry register, then read omega from the popped
        tail of the omega-path delay (or from the synaptic register at
        the same delay tail), then plasticity, then episodic write
        gate, then trace updates.

        On theta-admitted ticks (sin(2 pi f_theta t) > 0) omega is
        computed and emitted. On theta-suppressed ticks the substrate
        spikes normally but no omega is emitted, no episode is written,
        and (when theta_gated_rstdp is True) the R-STDP modulation flips
        sign per the falling-phase rule.

        Args:
            coords: (B, coordinate_dim) external coordinate input.
            interoceptive: optional (B, proto_self_dim) afferent.
            external_neuromod: optional (B, 4) DA, NE, ACh, 5-HT.
            theta_phase: optional float in [0, 2 pi). When provided,
                the loop uses this as its theta phase for this tick
                (allowing multiple populations to share a phase
                reference). When None, the internal phase advances by
                dt * f_theta_hz as before.

        Returns:
            IamLoopOutput.
        """
        if (
            coords.dim() != 2
            or coords.shape[-1] != self.cfg.coordinate_dim
        ):
            raise ValueError(
                f"coords must be (B, {self.cfg.coordinate_dim}); "
                f"got {tuple(coords.shape)}"
            )
        batch = coords.shape[0]
        device = coords.device

        self._ensure_substrate_states(batch, device)

        if interoceptive is None:
            interoceptive = torch.zeros(
                batch, self.cfg.proto_self_dim, device=device,
            )

        prior_pcc_trace = (
            self.pcc_trace.to(device).detach().clone()
        )
        prior_predicted = (
            self.pcc_predicted_trace.to(device).detach().clone()
        )

        # ---- Proto-self forward ----
        proto_current = (
            torch.sigmoid(interoceptive)
            + self.cfg.proto_baseline_drive
        )
        proto_spikes, self._proto_state = self.proto_self(
            proto_current, self._proto_state,
        )

        # ---- Autobiographical readback (Eq. 27 fast term) ----
        autobio_drive = self._compute_autobio_readback(
            batch=batch, device=device,
        )

        # ---- Slow self-feed (Eq. 27 integral term) ----
        slow_self_feed = (
            self.cfg.zeta_slow * self.reentry_trace.to(device)
        )

        # ---- Pop the delayed MPFC inhibition ----
        # Decoupled from the omega-path delay; defaults to 1 tick. The
        # deque holds at most inhibitory_return_ticks past inhibitions.
        if (
            len(self._mpfc_delay_buffer)
            >= self._inhibitory_delay_ticks
        ):
            mpfc_inhibition_at_pcc = self._mpfc_delay_buffer[0]
        else:
            mpfc_inhibition_at_pcc = torch.zeros(
                batch, self.cfg.pcc_dim, device=device,
            )

        # ---- PCC forward ----
        # Drive composition follows the corpus Equation 21 with the
        # corpus-specified additions for autobio readback (Eq 27 fast
        # term) and slow self-feed (Eq 27 integral term), plus the
        # delayed MPFC inhibition and the engineering tonic baseline.
        # The proto_self placement is configurable per
        # cfg.proto_self_placement.
        coord_current = coords @ self.W_coord_to_pcc.t()
        proto_pcc_current = proto_spikes @ self.W_proto_to_pcc.t()
        cascade_in_tanh = coord_current
        if self.cfg.proto_self_placement == "inside_tanh":
            cascade_in_tanh = cascade_in_tanh + proto_pcc_current
            outside_tanh = 0.0
            cascade_after_tanh = torch.tanh(cascade_in_tanh)
        elif self.cfg.proto_self_placement == "outside_tanh":
            outside_tanh = proto_pcc_current
            cascade_after_tanh = torch.tanh(cascade_in_tanh) + outside_tanh
        else:  # 'modulator'
            # Multiplicative gate: proto-self scales the cascade
            # contribution. sigmoid(proto_pcc_current) is in [0, 1] and
            # multiplies the post-tanh cascade.
            gate = torch.sigmoid(proto_pcc_current)
            cascade_after_tanh = gate * torch.tanh(cascade_in_tanh)
        # Equation 27 fast term enters PCC drive AFTER the tanh per the
        # corpus Eq 21 form: the tanh wraps the cascade content; the
        # autobio readout enters the PCC z_epi pathway.
        pcc_input = (
            cascade_after_tanh
            + self.cfg.zeta_fast * autobio_drive
            + slow_self_feed
            + mpfc_inhibition_at_pcc
            + self.cfg.pcc_baseline_drive
        )
        pcc_spikes, self._pcc_state = self.pcc(
            pcc_input, self._pcc_state,
        )

        # ---- IPL forward (Eq. 24) ----
        ipl_input = (
            pcc_spikes @ self.W_pcc_to_ipl.t()
            + self.cfg.ipl_baseline_drive
        )
        ipl_spikes, self._ipl_state = self.ipl(
            ipl_input, self._ipl_state,
        )

        # ---- MPFC forward (Eq. 22) ----
        mpfc_input = (
            pcc_spikes @ self.W_pcc_to_mpfc.t()
            + ipl_spikes @ self.W_ipl_to_mpfc.t()
            + self.cfg.mpfc_baseline_drive
        )
        mpfc_spikes, self._mpfc_state = self.mpfc(
            mpfc_input, self._mpfc_state,
        )

        # ---- Push new MPFC inhibition into the inhibitory return deque ----
        new_mpfc_drive = (mpfc_spikes @ self.W_mpfc_to_pcc.t()).detach()
        self._mpfc_delay_buffer.append(new_mpfc_drive)

        # ---- Regulated re-entry r_reentry (Eq. 25, three-term form) ----
        # r_reentry(t) = r_PCC(t) + r_PCC_inh(t) + W_IPL_to_PCC . r_IPL(t)
        # The corpus equation has W_IPL_to_MPFC labelling the third
        # term; the dimensional reading is that this projection brings
        # IPL into the same space as r_reentry (which is PCC space, by
        # virtue of feeding back into PCC via Equation 27). The
        # W_ipl_to_reentry buffer is the fixed (non-learnable)
        # projection that does that mapping.
        r_pcc_inh_in_pcc = -new_mpfc_drive  # MPFC inhibition in PCC space
        ipl_contribution = ipl_spikes @ self.W_ipl_to_reentry.t()
        r_reentry = pcc_spikes + r_pcc_inh_in_pcc + ipl_contribution

        # ---- Update reentry_trace (Eq. 27 integral) ----
        self.reentry_trace = (
            self._reentry_decay * self.reentry_trace.to(device)
            + (1.0 - self._reentry_decay) * r_reentry.detach()
        )

        # ---- Push r_reentry onto the omega-path delay buffer (Eq. 26) ----
        # The corpus's tau_160 sits between r_reentry emission and
        # omega delivery. We push the current-tick r_reentry's batch-
        # mean here; Psi will read the popped tail.
        r_reentry_mean = r_reentry.detach().mean(dim=0)
        self._omega_delay_buffer.append(r_reentry_mean)

        # ---- Update the synaptic re-entry register (Eq. 29 and 30) ----
        # Discrete-state encoding so the signal survives the omega-path
        # delay buffer transit without floating-point drift.
        if self.synaptic_reentry is not None:
            self.synaptic_reentry.update(r_reentry_mean)

        # ---- Novelty and prediction error ----
        mean_pcc = pcc_spikes.detach().mean(dim=0)
        novelty = self._compute_novelty(
            mean_pcc, prior_pcc_trace, prior_predicted,
        )
        prediction_error = torch.mean(
            torch.abs(mean_pcc - prior_predicted)
        ).clamp(0.0, 1.0)

        # ---- Theta phase: external override or internal advance ----
        dt_s = self.cfg.dt_ms / 1000.0
        if theta_phase is not None:
            self._theta_phase = float(theta_phase) % (2.0 * math.pi)
        else:
            self._theta_phase = (
                self._theta_phase
                + 2.0 * math.pi * self.cfg.f_theta_hz * dt_s
            ) % (2.0 * math.pi)
        if self.cfg.enable_theta_gate:
            theta_admitted = math.sin(self._theta_phase) > 0.0
        else:
            theta_admitted = True

        # ---- Read r_reentry at the omega-path delay tail ----
        # Equation 26 reads r_reentry(t - tau_160). When the synaptic
        # register is enabled it provides the noise-stable form of
        # the same signal at the same delay; otherwise the float
        # deque's tail is used. Until the deque fills (the first
        # ignition_latency_ticks of cold boot), we read zeros, which
        # is the loop having no past re-entry to project from yet.
        if (
            len(self._omega_delay_buffer)
            >= self._omega_delay_ticks
        ):
            r_reentry_delayed = self._omega_delay_buffer[0]
        else:
            r_reentry_delayed = torch.zeros(
                self.cfg.pcc_dim, device=device,
            )
        # If the synaptic register is enabled, prefer its discrete-state
        # read. Functionally close to r_reentry_delayed but quantized;
        # the corpus 11a commitment is that this is the canonical
        # source for omega when the register is in use.
        if self.synaptic_reentry is not None:
            r_reentry_for_omega = (
                self.synaptic_reentry.read().to(device)
            )
        else:
            r_reentry_for_omega = r_reentry_delayed

        # ---- Compute omega and Phi(omega) on theta-admitted ticks ----
        omega: Optional[Tensor] = None
        omega_binding: Optional[Tensor] = None
        if theta_admitted:
            omega = self._compute_omega(
                r_reentry=r_reentry_for_omega.unsqueeze(0),
                novelty=novelty,
                prediction_error=prediction_error,
                external_neuromod=external_neuromod,
                device=device,
            )
            omega_binding = self.phi(omega)
            self.condition_trace.copy_(
                self.cfg.condition_trace_tau
                * self.condition_trace.to(device)
                + (1.0 - self.cfg.condition_trace_tau)
                * omega.detach(),
            )
            # Update the omega rolling window for maturity-from-variance.
            self._omega_window.append(omega.detach().cpu().clone())

        # ---- R-STDP on all six matrices ----
        # Modulation routing per Florian (2007) plus the corpus
        # commitments about which channels gate which projections:
        #   coord->pcc, proto->pcc, pcc->ipl: DA + novelty
        #   pcc->mpfc, ipl->mpfc:             DA + arousal
        #   mpfc->pcc:                        NE + novelty
        # Theta gating (when cfg.theta_gated_rstdp is True) flips the
        # sign of modulation on the falling phase, implementing the
        # "potentiation rising, depression falling" pattern from
        # Hyman et al. (2003) and related theta-modulated plasticity
        # literature.
        mod_omega = (
            omega if omega is not None
            else self.condition_trace.to(device)
        )
        dopamine = float(mod_omega[0].item())
        norepinephrine = float(mod_omega[1].item())
        nov_scalar = float(novelty.item())
        arousal_scalar = float(mod_omega[5].item())
        mod_input = 1.0 + dopamine + nov_scalar
        mod_pcc_outward = 1.0 + dopamine + nov_scalar
        mod_to_mpfc = 1.0 + dopamine + arousal_scalar
        mod_inhibitory = 1.0 + norepinephrine + nov_scalar
        if self.cfg.theta_gated_rstdp and not theta_admitted:
            # Falling theta phase: depression-only contribution.
            sign = -self.cfg.rstdp_depression_ratio
            mod_input *= sign
            mod_pcc_outward *= sign
            mod_to_mpfc *= sign
            mod_inhibitory *= sign

        coord_pre = coords.detach().mean(dim=0).clamp(0.0, 1.0)
        self.rstdp_coord_to_pcc.update(
            self.W_coord_to_pcc.data,
            coord_pre, mean_pcc, mod_input,
        )
        proto_pre = proto_spikes.detach().mean(dim=0)
        self.rstdp_proto_to_pcc.update(
            self.W_proto_to_pcc.data,
            proto_pre, mean_pcc, mod_input,
        )
        ipl_post = ipl_spikes.detach().mean(dim=0)
        self.rstdp_pcc_to_ipl.update(
            self.W_pcc_to_ipl.data,
            mean_pcc, ipl_post, mod_pcc_outward,
        )
        mpfc_post = mpfc_spikes.detach().mean(dim=0)
        self.rstdp_pcc_to_mpfc.update(
            self.W_pcc_to_mpfc.data,
            mean_pcc, mpfc_post, mod_to_mpfc,
        )
        self.rstdp_ipl_to_mpfc.update(
            self.W_ipl_to_mpfc.data,
            ipl_post, mpfc_post, mod_to_mpfc,
        )
        self.rstdp_mpfc_to_pcc.update(
            self.W_mpfc_to_pcc.data,
            mpfc_post, mean_pcc, mod_inhibitory,
        )

        # ---- Episodic write gate ----
        stored = False
        if (
            theta_admitted
            and omega is not None
            and self._can_store(novelty)
        ):
            self._store_episode(
                pcc_spikes=mean_pcc,
                mpfc_spikes=mpfc_post,
                prior_pcc_trace=prior_pcc_trace,
                predicted_pcc_trace=prior_predicted,
                operating_condition=omega.detach(),
                novelty=novelty,
                prediction_error=prediction_error,
                source=coords.detach(),
            )
            stored = True

        # ---- Update persistent traces and tick counter ----
        self._update_traces(
            mean_pcc=mean_pcc,
            mean_mpfc_inhibition=new_mpfc_drive.mean(dim=0).detach(),
        )
        self.tick.add_(1)
        self._tick_count_for_maturity += 1

        # ---- Diagnostics by level ----
        diagnostics = self._build_diagnostics(
            theta_admitted=theta_admitted,
            mean_pcc=mean_pcc,
            mpfc_post=mpfc_post,
            ipl_post=ipl_post,
            proto_pre=proto_pre,
            novelty=novelty,
            prediction_error=prediction_error,
            stored=stored,
        )

        return IamLoopOutput(
            pcc_spikes=pcc_spikes,
            mpfc_spikes=mpfc_spikes,
            ipl_spikes=ipl_spikes,
            proto_self_spikes=proto_spikes,
            r_reentry=r_reentry,
            omega=omega,
            omega_binding=omega_binding,
            novelty=float(novelty.item()),
            prediction_error=float(prediction_error.item()),
            stored_episode=stored,
            theta_admitted=theta_admitted,
            diagnostics=diagnostics,
        )

    def _build_diagnostics(
        self,
        theta_admitted: bool,
        mean_pcc: Tensor,
        mpfc_post: Tensor,
        ipl_post: Tensor,
        proto_pre: Tensor,
        novelty: Tensor,
        prediction_error: Tensor,
        stored: bool,
    ) -> Dict[str, float]:
        """Build the per-tick diagnostics dict at cfg.diagnostics_level.

        Level 0: empty dict (production).
        Level 1: minimal set (tick, theta_admitted, pcc_rate, novelty,
            n_episodes, stored_episode).
        Level 2: full set (legacy default).
        """
        if self.cfg.diagnostics_level == 0:
            return {}
        d: Dict[str, float] = {
            "tick": float(self.tick.item()),
            "theta_admitted": float(1.0 if theta_admitted else 0.0),
            "pcc_spike_rate": float(mean_pcc.mean().item()),
            "novelty": float(novelty.item()),
            "n_episodes": float(len(self._episodes)),
            "stored_episode": float(1.0 if stored else 0.0),
        }
        if self.cfg.diagnostics_level == 1:
            return d
        # Level 2: full set.
        d.update({
            "theta_phase": self._theta_phase,
            "mpfc_spike_rate": float(mpfc_post.mean().item()),
            "ipl_spike_rate": float(ipl_post.mean().item()),
            "proto_self_spike_rate": float(proto_pre.mean().item()),
            "prediction_error": float(prediction_error.item()),
            "omega_delay_filled": float(
                len(self._omega_delay_buffer)
                / max(self._omega_delay_ticks, 1)
            ),
            "inhibitory_delay_filled": float(
                len(self._mpfc_delay_buffer)
                / max(self._inhibitory_delay_ticks, 1)
            ),
            "W_pcc_to_mpfc_norm": float(
                self.W_pcc_to_mpfc.norm().item()
            ),
            "W_mpfc_to_pcc_norm": float(
                self.W_mpfc_to_pcc.norm().item()
            ),
            "maturity": float(self._maturity()),
        })
        return d

    def _compute_autobio_readback(
        self, batch: int, device: torch.device,
    ) -> Tensor:
        """Compute the autobiographical readback drive on PCC.

        This is the fast term of corpus Equation 27. The corpus form is
        zeta_fast * m_CA3(t), where m_CA3 is the most recent CA3
        attractor settle from a hippocampal kernel (Section 9). When
        cfg.m_ca3_provider is supplied (a callable returning a
        (pcc_dim,) tensor), this method calls it and returns the
        provider's output broadcast to (B, pcc_dim) and scaled.

        When no provider is configured, the method falls back to
        sampling the local episode store: top-N episodes by omega
        similarity to the current condition_trace, mean over their
        stored PCC spikes, broadcast to batch. This is the cold-boot
        substitute for an unwired hippocampal kernel; documented as a
        substitute rather than the canonical implementation.

        Returns zeros when the store is empty AND no provider is set.

        Reference: PRAGMI Complete Mathematical Corpus Eq. 27 plus
        Section 9b on the autobiographical readback.
        """
        # Provider path: use the upstream kernel's CA3 settle directly.
        if self.cfg.m_ca3_provider is not None:
            try:
                m_ca3 = self.cfg.m_ca3_provider()
            except Exception:
                m_ca3 = None
            if m_ca3 is not None:
                m_ca3 = m_ca3.detach().to(device).float()
                if m_ca3.dim() == 1:
                    if m_ca3.numel() != self.cfg.pcc_dim:
                        raise ValueError(
                            f"m_ca3_provider returned shape "
                            f"{tuple(m_ca3.shape)}; expected "
                            f"({self.cfg.pcc_dim},) or "
                            f"(B, {self.cfg.pcc_dim})"
                        )
                    m_ca3 = m_ca3.unsqueeze(0).expand(batch, -1)
                elif m_ca3.dim() == 2:
                    if m_ca3.shape[-1] != self.cfg.pcc_dim:
                        raise ValueError(
                            f"m_ca3_provider returned shape "
                            f"{tuple(m_ca3.shape)}; expected last "
                            f"dim {self.cfg.pcc_dim}"
                        )
                else:
                    raise ValueError(
                        f"m_ca3_provider must return 1d or 2d, got "
                        f"{m_ca3.dim()}d"
                    )
                return (
                    self.cfg.autobio_readback_scale * m_ca3
                ).contiguous()
        # Fallback: local-episode-store substitute.
        if not self._episodes:
            return torch.zeros(
                batch, self.cfg.pcc_dim, device=device,
            )
        query = self.condition_trace.to(device).detach()
        drawn = RetrievalDraw.draw_past(
            self._episodes,
            query_omega=query,
            n=self.cfg.autobio_readback_n,
        )
        if not drawn:
            return torch.zeros(
                batch, self.cfg.pcc_dim, device=device,
            )
        stacked = torch.stack(
            [ep.pcc_spikes.to(device) for ep in drawn], dim=0,
        )
        mean_pcc = stacked.mean(dim=0)
        return (
            self.cfg.autobio_readback_scale * mean_pcc
        ).unsqueeze(0).expand(batch, -1).contiguous()

    def _compute_omega(
        self,
        r_reentry: Tensor,
        novelty: Tensor,
        prediction_error: Tensor,
        external_neuromod: Optional[Tensor],
        device: torch.device,
    ) -> Tensor:
        """Compute the eight-component omega vector from loop dynamics.

        Components 0-3 (DA, NE, ACh, 5-HT) come from external_neuromod when
        provided, else from neutral defaults. Components 4-7 (novelty,
        arousal, valence, maturity) are computed from the loop's own state.
        """
        if external_neuromod is None:
            da = torch.tensor(0.0, device=device)
            ne = torch.tensor(0.0, device=device)
            ach = torch.tensor(0.5, device=device)
            five_ht = torch.tensor(0.0, device=device)
        else:
            nm = external_neuromod.detach().to(device).float()
            if nm.dim() != 2 or nm.shape[-1] != 4:
                raise ValueError(
                    f"external_neuromod must be (B, 4), got "
                    f"{tuple(nm.shape)}"
                )
            nm_mean = nm.mean(dim=0)
            da = nm_mean[0].clamp(-1.0, 1.0)
            ne = nm_mean[1].clamp(0.0, 1.0)
            ach = nm_mean[2].clamp(0.0, 1.0)
            five_ht = nm_mean[3].clamp(-1.0, 1.0)

        r_mean = r_reentry.detach().mean(dim=0)
        arousal = torch.tanh(
            r_mean.norm() / math.sqrt(self.cfg.pcc_dim)
        )
        valence = torch.tanh(r_mean.mean())
        maturity = torch.tensor(self._maturity(), device=device)

        omega = torch.stack(
            [
                da.to(device),
                ne.to(device),
                ach.to(device),
                five_ht.to(device),
                novelty.to(device).clamp(0.0, 1.0),
                arousal.to(device).clamp(0.0, 1.0),
                valence.to(device),
                maturity.to(device).clamp(0.0, 1.0),
            ],
            dim=0,
        )
        return omega

    def _compute_novelty(
        self,
        mean_pcc: Tensor,
        prior_trace: Tensor,
        prior_predicted: Tensor,
    ) -> Tensor:
        """CA1-style mismatch comparator.

        Reference: Lisman and Grace (2005). DOI: 10.1016/j.neuron.2005.05.002.
        """
        device = mean_pcc.device
        trace_mismatch = torch.mean(
            torch.abs(mean_pcc - prior_trace.to(device))
        )
        pred_mismatch = torch.mean(
            torch.abs(mean_pcc - prior_predicted.to(device))
        )
        return (
            0.5 * trace_mismatch + 0.5 * pred_mismatch
        ).clamp(0.0, 1.0)

    def _can_store(self, novelty: Tensor) -> bool:
        """Novelty above threshold AND refractory cooldown elapsed."""
        if float(novelty.item()) <= self.cfg.novelty_threshold:
            return False
        return int(
            self.tick.item() - self.last_store_tick.item()
        ) >= self.cfg.store_cooldown_ticks

    def _store_episode(
        self,
        pcc_spikes: Tensor,
        mpfc_spikes: Tensor,
        prior_pcc_trace: Tensor,
        predicted_pcc_trace: Tensor,
        operating_condition: Tensor,
        novelty: Tensor,
        prediction_error: Tensor,
        source: Tensor,
    ) -> None:
        """Write one episode through the WriteGate."""
        source_bytes = (
            source.detach().cpu().contiguous().numpy().tobytes()
        )
        record = EpisodeRecord(
            tick=int(self.tick.item()),
            pcc_spikes=pcc_spikes.detach().cpu().clone(),
            mpfc_spikes=mpfc_spikes.detach().cpu().clone(),
            prior_pcc_trace=prior_pcc_trace.detach().cpu().clone(),
            predicted_pcc_trace=(
                predicted_pcc_trace.detach().cpu().clone()
            ),
            operating_condition=(
                operating_condition.detach().cpu().clone()
            ),
            novelty=float(novelty.item()),
            prediction_error=float(prediction_error.item()),
            source_sha256=hashlib.sha256(source_bytes).hexdigest(),
            structural_hash=self.structural_hash(),
            tau_proj=0,
        )
        WriteGate.write(
            self._episodes, record, self.cfg.max_episodes,
            eviction=self.cfg.eviction_policy,
        )
        self.last_store_tick.copy_(self.tick)

    def _update_traces(
        self,
        mean_pcc: Tensor,
        mean_mpfc_inhibition: Tensor,
    ) -> None:
        """Update the PCC trace EMA and the predicted-trace EMA."""
        device = self.pcc_trace.device
        pcc = mean_pcc.detach().to(device)
        self.pcc_trace.mul_(self.cfg.pcc_trace_tau).add_(
            (1.0 - self.cfg.pcc_trace_tau) * pcc
        )
        carry_mean = mean_mpfc_inhibition.to(device)
        predicted = (
            pcc - 0.3 * carry_mean.clamp(min=0.0)
        ).clamp(0.0, 1.0)
        self.pcc_predicted_trace.mul_(
            self.cfg.pcc_predicted_trace_tau
        ).add_(
            (1.0 - self.cfg.pcc_predicted_trace_tau) * predicted
        )

    def _maturity(self) -> float:
        """Return the current maturity scalar in [0, 1].

        Two computation modes per cfg.maturity_mode:

        'tick_count': linear ramp from 0 at construction to 1 at
            cfg.maturity_horizon_ticks. Simple, arbitrary horizon.

        'omega_variance' (default): rolling-window variance of recent
            omega vectors. The trace of the per-component variance is
            mapped linearly through cfg.maturity_var_low ->
            cfg.maturity_var_high to maturity 1.0 -> 0.0 (high variance
            means the loop is still drifting; low variance means the
            operating-condition distribution has settled). Returns 0
            when the window is empty.
        """
        if self.cfg.maturity_mode == "tick_count":
            if self.cfg.maturity_horizon_ticks <= 0:
                return 1.0
            m = (
                self._tick_count_for_maturity
                / self.cfg.maturity_horizon_ticks
            )
            return min(1.0, max(0.0, m))
        # omega_variance mode.
        if len(self._omega_window) < 2:
            return 0.0
        stacked = torch.stack(list(self._omega_window), dim=0)
        # Per-component variance, then trace (sum of variances).
        variance = stacked.var(dim=0, unbiased=False)
        var_trace = float(variance.sum().item())
        # Linear map: var_trace at or below var_low -> 1.0;
        # at or above var_high -> 0.0.
        lo = self.cfg.maturity_var_low
        hi = self.cfg.maturity_var_high
        if var_trace <= lo:
            return 1.0
        if var_trace >= hi:
            return 0.0
        return float((hi - var_trace) / (hi - lo))

    def reset(self, level: str = "turn") -> None:
        """Scoped reset.

        Args:
            level: 'turn' resets per-batch substrate states and the two
                conduction-delay buffers and the omega window.
                'episode' additionally clears persistent traces, R-STDP
                eligibility, the synaptic re-entry register,
                last_store_tick, and the theta phase. Stored episodes
                are retained unless clear_episodes is called explicitly.
        """
        if level not in {"turn", "episode"}:
            raise ValueError(
                f"level must be 'turn' or 'episode', got {level!r}"
            )
        self._proto_state = None
        self._pcc_state = None
        self._mpfc_state = None
        self._ipl_state = None
        self._mpfc_delay_buffer.clear()
        self._omega_delay_buffer.clear()
        self._omega_window.clear()
        if level == "episode":
            self.pcc_trace.zero_()
            self.pcc_predicted_trace.zero_()
            self.condition_trace.zero_()
            with torch.no_grad():
                self.reentry_trace.zero_()
            for r in [
                self.rstdp_coord_to_pcc, self.rstdp_proto_to_pcc,
                self.rstdp_pcc_to_mpfc, self.rstdp_pcc_to_ipl,
                self.rstdp_ipl_to_mpfc, self.rstdp_mpfc_to_pcc,
            ]:
                r.reset()
            if self.synaptic_reentry is not None:
                self.synaptic_reentry.reset()
            self.last_store_tick.fill_(-(10 ** 9))
            self._theta_phase = 0.0

    def clear_episodes(self) -> None:
        """Delete all stored episodes."""
        self._episodes.clear()

    def episode_count(self) -> int:
        """Return the number of stored episodes."""
        return len(self._episodes)

    def latest_episode(self) -> Optional[EpisodeRecord]:
        """Return the most recent episode, or None if empty."""
        return self._episodes[-1] if self._episodes else None

